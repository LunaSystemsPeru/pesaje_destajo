    private void btn_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarActionPerformed
        jLabel9.setText("AGREGAR NUEVO SUB PARAMETRO");
        txt_concepto.setEnabled(true);
        txt_valor.setEnabled(true);
        btn_grabar.setEnabled(true);
        btn_agregar.setEnabled(false);
        c_detalle.setIddetalle(0);
        txt_concepto.requestFocus();
    }//GEN-LAST:event_btn_agregarActionPerformed

    private void t_parametrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_parametrosMouseClicked
        if (evt.getClickCount() == 2) {
            int fila_seleccionada = t_parametros.getSelectedRow();
            jLabel12.setText(t_parametros.getValueAt(fila_seleccionada, 1).toString());
            c_detalle.setIdparametro(Integer.parseInt(t_parametros.getValueAt(fila_seleccionada, 0).toString()));
            c_detalle.mostrar(t_detalle);
            btn_agregar.setEnabled(true);
        }
    }//GEN-LAST:event_t_parametrosMouseClicked

    private void t_detalleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_t_detalleMouseClicked
        if (evt.getClickCount() == 2) {
            int fila_sub = t_detalle.getSelectedRow();
            c_detalle.setIddetalle(Integer.parseInt(t_detalle.getValueAt(fila_sub, 0).toString()));
            c_detalle.obtener_datos();
            txt_concepto.setText(c_detalle.getNombre());
            txt_valor.setText(c_detalle.getValor());
            txt_item.setText(c_detalle.getIddetalle() + "");
            txt_concepto.setEnabled(true);
            txt_valor.setEnabled(true);
            txt_concepto.requestFocus();
            btn_grabar.setEnabled(true);
        }
    }//GEN-LAST:event_t_detalleMouseClicked

    private void btn_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_grabarActionPerformed
        c_detalle.setNombre(txt_concepto.getText().trim().toUpperCase());
        c_detalle.setValor(txt_valor.getText().trim().toUpperCase());

        if (c_detalle.getIddetalle() == 0) {
            c_detalle.obtener_codigo();
            c_detalle.registrar();
        } else {
            c_detalle.actualizar();
        }
        
        c_detalle.mostrar(t_detalle);

        txt_item.setText("");
        txt_concepto.setText("");
        txt_valor.setText("");
        txt_concepto.setEnabled(false);
        txt_valor.setEnabled(false);
        btn_grabar.setEnabled(false);
        btn_agregar.setEnabled(true);
    }//GEN-LAST:event_btn_grabarActionPerformed


